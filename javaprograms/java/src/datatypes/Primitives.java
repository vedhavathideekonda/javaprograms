package datatypes;

public class Primitives {

	public static void main(String[] args) {
	int a = 10;
	char b ='A';
	float c = 5.0f;
	boolean d =true;
	double e = 4.4d;
	System.out.println("integer is "+ a);
	System.out.println("character is "+ b);
	System.out.println("float is "+ c);
	System.out.println("boolean is "+ d);
	System.out.println("double is "+ e);

	}

}
