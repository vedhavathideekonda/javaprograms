package implictexplicitdata;

public class ExplicitConversion {
	public static void main(String[] args) {
		float a =100;
	System.out.println("float representation is "+ a);
	long b =(long)a;
	System.out.println("long representation is "+ b);
	int c =(int)b;
	System.out.println("ont representation is "+ c);
	}
}
